/* =========================================================================
   SCERT Study Notes — app shell (vanilla JS, no dependencies)
   Consumes window.__META__ / __NAV__ / __NOTES__ / __HOME__ / __COVERAGE__
   from assets/notes-data.js (generated by _tools/build_site.py).
   ========================================================================= */
(function () {
  "use strict";

  var META = window.__META__ || {};
  var NAV = window.__NAV__ || [];
  var NOTES = window.__NOTES__ || {};
  var HOME = window.__HOME__ || "";
  var COVERAGE = window.__COVERAGE__ || "";

  var SECTION_EMOJI = ["📜", "🌍", "💰", "⚖️", "🏛️", "📋"];

  var $ = function (sel, root) { return (root || document).querySelector(sel); };
  var $$ = function (sel, root) { return Array.prototype.slice.call((root || document).querySelectorAll(sel)); };

  function cleanSectionTitle(t) { return t.replace(/^\([ivxlcdm]+\)\s*/i, ""); }
  function escapeHtml(s) {
    return s.replace(/[&<>"']/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c];
    });
  }
  function escapeReg(s) { return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"); }

  /* ----------------------------- Theme ---------------------------------- */
  var themeBtn = $("#themeBtn");
  function applyTheme(t) { document.documentElement.setAttribute("data-theme", t); }
  // Shared with the PSC English app: same storage key + default (dark), so the
  // light/dark choice carries across both sections of the combined site.
  (function initTheme() {
    var saved = null;
    try { saved = localStorage.getItem("psc_theme"); } catch (e) {}
    if (!saved) saved = "dark";
    applyTheme(saved);
  })();
  themeBtn.addEventListener("click", function () {
    var cur = document.documentElement.getAttribute("data-theme") === "dark" ? "light" : "dark";
    applyTheme(cur);
    try { localStorage.setItem("psc_theme", cur); } catch (e) {}
  });

  /* ----------------------------- Sidebar -------------------------------- */
  var navEl = $("#nav");
  function buildNav() {
    var html = "";
    NAV.forEach(function (sec, i) {
      var label = escapeHtml(cleanSectionTitle(sec.title));
      var emoji = SECTION_EMOJI[i] || "📁";
      var count = 0;
      sec.groups.forEach(function (g) { count += g.items.length; });
      html += '<div class="nav-section collapsed" data-sec="' + i + '">';
      html += '<button class="nav-sec-btn" data-toggle="' + i + '">' +
              '<span>' + emoji + "</span><span>" + label + "</span>" +
              '<span class="nav-count">' + count + "</span>" +
              '<span class="chev">▾</span></button>';
      html += '<div class="nav-sec-body">';
      sec.groups.forEach(function (g) {
        if (g.title) html += '<div class="nav-group-label">' + escapeHtml(g.title) + "</div>";
        g.items.forEach(function (it) {
          html += '<a class="nav-link" href="#/' + it.id + '" data-id="' + it.id + '">' +
                  '<span>' + escapeHtml(it.title) + "</span>" +
                  (it.warn ? '<span class="warn" title="Limited/no SCERT coverage">⚠️</span>' : "") +
                  "</a>";
        });
      });
      html += "</div></div>";
    });
    navEl.innerHTML = html;
  }
  navEl.addEventListener("click", function (e) {
    var btn = e.target.closest(".nav-sec-btn");
    if (btn) {
      var sec = btn.closest(".nav-section");
      sec.classList.toggle("collapsed");
      return;
    }
    if (e.target.closest(".nav-link")) closeMobileNav();
  });

  function setActive(id) {
    $$(".nav-link").forEach(function (a) { a.classList.remove("active"); });
    var link = navEl.querySelector('.nav-link[data-id="' + (window.CSS && CSS.escape ? CSS.escape(id) : id) + '"]');
    if (link) {
      link.classList.add("active");
      var sec = link.closest(".nav-section");
      if (sec) sec.classList.remove("collapsed");
      link.scrollIntoView({ block: "nearest" });
    }
  }

  /* ----------------------------- Mobile nav ----------------------------- */
  var backdrop = $("#backdrop");
  function openMobileNav() { document.body.classList.add("nav-open"); backdrop.hidden = false; }
  function closeMobileNav() { document.body.classList.remove("nav-open"); backdrop.hidden = true; }
  $("#menuBtn").addEventListener("click", function () {
    document.body.classList.contains("nav-open") ? closeMobileNav() : openMobileNav();
  });
  backdrop.addEventListener("click", closeMobileNav);

  /* ----------------------------- Rendering ------------------------------ */
  var viewEl = $("#view");

  function enhanceProse(root) {
    // wrap tables for horizontal scroll
    $$("table", root).forEach(function (tb) {
      if (tb.parentElement.classList.contains("table-wrap")) return;
      var wrap = document.createElement("div");
      wrap.className = "table-wrap";
      tb.parentNode.insertBefore(wrap, tb);
      wrap.appendChild(tb);
    });
    // flag ⚠️ blockquotes as warnings
    $$("blockquote", root).forEach(function (bq) {
      if (bq.textContent.indexOf("⚠️") !== -1) bq.classList.add("warn");
    });
  }

  function renderHome() {
    var cards = "";
    NAV.forEach(function (sec, i) {
      var first = null, count = 0;
      sec.groups.forEach(function (g) { count += g.items.length; if (!first && g.items[0]) first = g.items[0].id; });
      cards += '<a class="section-card" href="#/' + (first || "") + '">' +
               '<div class="sc-emoji">' + (SECTION_EMOJI[i] || "📁") + "</div>" +
               '<div class="sc-title">' + escapeHtml(cleanSectionTitle(sec.title)) + "</div>" +
               '<div class="sc-meta">' + count + " topics</div></a>";
    });
    var body = HOME.replace(/<h1[^>]*>[\s\S]*?<\/h1>/, ""); // drop duplicate H1
    viewEl.innerHTML =
      '<div class="home-hero"><h1>' + escapeHtml(META.title || "Study Notes") + "</h1>" +
      "<p>" + escapeHtml(META.subtitle || "") + "</p></div>" +
      '<div class="section-cards">' + cards + "</div>" +
      '<div class="prose">' + body + "</div>";
    enhanceProse(viewEl);
    document.title = META.title || "Study Notes";
  }

  function renderStatic(htmlStr, title) {
    viewEl.innerHTML = '<article><h1 class="note-title">' + escapeHtml(title) + "</h1>" +
                       '<div class="prose">' + htmlStr + "</div></article>";
    enhanceProse(viewEl);
    document.title = title + " · " + (META.title || "");
  }

  function renderNote(id) {
    var n = NOTES[id];
    if (!n) { renderMissing(id); return; }
    var meta = n.meta ? '<div class="note-meta">' + n.meta + "</div>" : "";
    viewEl.innerHTML = "<article>" +
      '<h1 class="note-title">' + escapeHtml(n.title) + "</h1>" +
      meta +
      '<div class="prose">' + n.html + "</div></article>";
    enhanceProse(viewEl);
    setActive(id);
    document.title = n.title + " · " + (META.title || "");
  }

  function renderMissing(id) {
    viewEl.innerHTML = '<article><h1 class="note-title">Not found</h1>' +
      '<div class="prose"><p>No note matches <code>' + escapeHtml(id) + "</code>.</p>" +
      '<p><a href="#/">← Back to all notes</a></p></div></article>';
    document.title = "Not found · " + (META.title || "");
  }

  /* ----------------------------- Router --------------------------------- */
  function route() {
    var raw = location.hash.replace(/^#\/?/, "");
    var parts = raw.split("#");
    var id = decodeURIComponent(parts[0] || "");
    var anchor = parts[1] || "";
    $$(".nav-link").forEach(function (a) { a.classList.remove("active"); });

    if (id === "" ) { renderHome(); }
    else if (id === "coverage") { renderStatic(COVERAGE, "Coverage Report"); }
    else if (NOTES[id]) { renderNote(id); }
    else { renderMissing(id); }

    closeMobileNav();
    if (anchor) {
      var t = document.getElementById(anchor);
      if (t) { t.scrollIntoView(); return; }
    }
    $("#content").scrollTop = 0;
    window.scrollTo(0, 0);
  }
  window.addEventListener("hashchange", route);

  /* ----------------------------- Lightbox ------------------------------- */
  var lb = $("#lightbox"), lbImg = $("#lightboxImg"), lbCap = $("#lightboxCap");
  viewEl.addEventListener("click", function (e) {
    var img = e.target.closest(".prose img");
    if (!img) return;
    lbImg.src = img.getAttribute("src");
    var em = img.nextElementSibling;
    lbCap.textContent = (em && em.tagName === "EM") ? em.textContent : (img.getAttribute("alt") || "");
    lb.hidden = false;
  });
  function closeLb() { lb.hidden = true; lbImg.src = ""; }
  lb.addEventListener("click", function (e) { if (e.target === lb || e.target.closest(".lightbox-close")) closeLb(); });

  /* ----------------------------- Search --------------------------------- */
  var searchInput = $("#searchInput"), resultsEl = $("#searchResults");
  var INDEX = Object.keys(NOTES).map(function (id) {
    var n = NOTES[id];
    return { id: id, title: n.title, section: cleanSectionTitle(n.section || ""), low: (n.title + " " + n.text).toLowerCase(), text: n.text };
  });
  var selIdx = -1, current = [];

  function snippet(text, terms) {
    var low = text.toLowerCase(), idx = -1;
    for (var i = 0; i < terms.length; i++) { var p = low.indexOf(terms[i]); if (p !== -1 && (idx === -1 || p < idx)) idx = p; }
    if (idx === -1) idx = 0;
    var start = Math.max(0, idx - 60), end = Math.min(text.length, start + 190);
    var s = (start > 0 ? "… " : "") + text.slice(start, end) + (end < text.length ? " …" : "");
    s = escapeHtml(s);
    terms.forEach(function (t) {
      s = s.replace(new RegExp("(" + escapeReg(escapeHtml(t)) + ")", "gi"), "<mark>$1</mark>");
    });
    return s;
  }

  function runSearch(q) {
    q = q.trim().toLowerCase();
    if (q.length < 2) { hideResults(); return; }
    var terms = q.split(/\s+/).filter(Boolean);
    var hits = [];
    INDEX.forEach(function (e) {
      var ok = terms.every(function (t) { return e.low.indexOf(t) !== -1; });
      if (!ok) return;
      var score = 0, tl = e.title.toLowerCase();
      terms.forEach(function (t) {
        if (tl.indexOf(t) !== -1) score += 10;
        if (tl.indexOf(t) === 0) score += 5;
      });
      hits.push({ e: e, score: score });
    });
    hits.sort(function (a, b) { return b.score - a.score || a.e.title.localeCompare(b.e.title); });
    current = hits.slice(0, 40);
    selIdx = -1;
    renderResults(terms);
  }

  function renderResults(terms) {
    if (!current.length) {
      resultsEl.innerHTML = '<div class="sr-empty">No matching notes.</div>';
      resultsEl.hidden = false; return;
    }
    var html = '<div class="sr-head">' + current.length + " result" + (current.length > 1 ? "s" : "") + "</div>";
    current.forEach(function (h, i) {
      html += '<a class="sr-item" href="#/' + h.e.id + '" data-i="' + i + '">' +
              '<div class="sr-title">' + escapeHtml(h.e.title) +
              '<span class="sr-sec">' + escapeHtml(h.e.section) + "</span></div>" +
              '<div class="sr-snip">' + snippet(h.e.text, terms) + "</div></a>";
    });
    resultsEl.innerHTML = html;
    resultsEl.hidden = false;
  }
  function hideResults() { resultsEl.hidden = true; resultsEl.innerHTML = ""; current = []; selIdx = -1; }
  function setSel(i) {
    var items = $$(".sr-item", resultsEl);
    items.forEach(function (el) { el.classList.remove("sel"); });
    if (i >= 0 && i < items.length) { items[i].classList.add("sel"); items[i].scrollIntoView({ block: "nearest" }); }
    selIdx = i;
  }

  searchInput.addEventListener("input", function () { runSearch(searchInput.value); });
  searchInput.addEventListener("focus", function () { if (searchInput.value.trim().length >= 2) runSearch(searchInput.value); });
  searchInput.addEventListener("keydown", function (e) {
    if (e.key === "ArrowDown") { e.preventDefault(); setSel(Math.min(selIdx + 1, current.length - 1)); }
    else if (e.key === "ArrowUp") { e.preventDefault(); setSel(Math.max(selIdx - 1, 0)); }
    else if (e.key === "Enter") {
      var pick = selIdx >= 0 ? current[selIdx] : current[0];
      if (pick) { location.hash = "#/" + pick.e.id; searchInput.blur(); hideResults(); }
    } else if (e.key === "Escape") { searchInput.value = ""; hideResults(); searchInput.blur(); }
  });
  resultsEl.addEventListener("click", function (e) { if (e.target.closest(".sr-item")) hideResults(); });
  document.addEventListener("click", function (e) {
    if (!e.target.closest(".search") && !e.target.closest(".search-results")) hideResults();
  });
  document.addEventListener("keydown", function (e) {
    if (e.key === "/" && document.activeElement !== searchInput && !/^(INPUT|TEXTAREA)$/.test(document.activeElement.tagName)) {
      e.preventDefault(); searchInput.focus(); searchInput.select();
    }
    if (e.key === "Escape" && !lb.hidden) closeLb();
  });

  /* ----------------------------- Init ----------------------------------- */
  buildNav();
  $("#buildInfo").textContent = (META.noteCount || 0) + " notes · built " + (META.built || "");
  if (!location.hash) location.hash = "#/";
  route();
})();
